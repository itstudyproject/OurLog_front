import React, { useEffect, useState } from "react";
import { useNavigate, useParams } from "react-router-dom";
import "../styles/WorkerPage.css";
import { fetchProfile, UserProfileDTO } from "../hooks/profileApi";

interface Post {
  postId: number;
  title: string;
  artist: string;
  highestBid: string;
  link: string;
  favoriteCnt?: number;
  liked?: boolean;
  isArtist?: boolean;
  originImagePath?: string;
  resizedImagePath?: string;
  thumbnailImagePath?: string;
  fileName?: string;
  pictureDTOList?: Array<{
    originImagePath?: string;
    resizedImagePath?: string;
    thumbnailImagePath?: string;
    fileName?: string;
  }> | null;
  boardNo?: number;
  imageUrl?: string;
}

const baseUrl = "http://localhost:8080/ourlog";

// ‚úÖ Ïù¥ÎØ∏ÏßÄ ÏÑúÎπôÏùÑ ÏúÑÌïú Î∞±ÏóîÎìú Î≤†Ïù¥Ïä§ URL Ï∂îÍ∞Ä (ÌïÑÏöîÏóê Îî∞Îùº ÏàòÏ†ïÌïòÏÑ∏Ïöî)
const imageBaseUrl = `http://localhost:8080/ourlog/picture/display/`; // ÏòàÏãú Í≤ΩÎ°ú, Ïã§Ï†ú Î∞±ÏóîÎìú Í≤ΩÎ°úÏóê ÎßûÍ≤å ÏàòÏ†ï ÌïÑÏöî

const WorkerPage: React.FC = () => {
  const { userId: paramUserId } = useParams<{ userId: string }>();
  const userId = Number(paramUserId);

  const rawLoggedInUserId = localStorage.getItem("user");
  let loggedInUserId: number = NaN;

  try {
    const parsedData = rawLoggedInUserId ? JSON.parse(rawLoggedInUserId) : null;
    if (parsedData && typeof parsedData.userId === "number") {
      loggedInUserId = parsedData.userId;
    }
  } catch (error) {
    console.error("‚ùå JSON ÌååÏã± Ïã§Ìå®:", error);
  }

  const [cardData, setCardData] = useState<Post[]>([]);
  const [currentPage, setCurrentPage] = useState(1);
  const [followCnt, setFollowCnt] = useState(0);
  const [followingCnt, setFollowingCnt] = useState(0);
  const [isFollowing, setIsFollowing] = useState(false);
  const [profile, setProfile] = useState<UserProfileDTO | null>(null);

  const navigate = useNavigate();

  const cardsPerPage = 6;
  const totalPages = Math.ceil(cardData.length / cardsPerPage);

  const currentCards = cardData.slice(
    (currentPage - 1) * cardsPerPage,
    currentPage * cardsPerPage
  );

  useEffect(() => {
    if (isNaN(userId) || userId <= 0) return;

    const token = localStorage.getItem("token");

    // ÌîÑÎ°úÌïÑ Í∞ÄÏ†∏Ïò§Í∏∞
    fetchProfile(userId)
      .then((data) => {
        if ("nickname" in data) {
          setProfile(data);
          if (typeof data.followCnt === "number") setFollowCnt(data.followCnt);
          if (typeof data.followingCnt === "number")
            setFollowingCnt(data.followingCnt);
        } else {
          console.error("ÌîÑÎ°úÌïÑ Îç∞Ïù¥ÌÑ∞ ÏóêÎü¨:", data);
          setProfile(null);
        }
      })
      .catch((err) => {
        console.error("fetchProfile Ïã§Ìå®:", err);
        setProfile(null);
      });

    // ÌåîÎ°úÏö∞ ÏÉÅÌÉú ÌôïÏù∏
    const checkFollowingStatus = async () => {
      if (!isNaN(loggedInUserId) && loggedInUserId !== userId) {
        try {
          const res = await fetch(
            `${baseUrl}/followers/status/isFollowing/${loggedInUserId}/${userId}`,
            {
              method: "GET",
              headers: {
                "Content-Type": "application/json",
                Authorization: `Bearer ${token}`,
              },
            }
          );
          if (res.ok) {
            const data = await res.json();
            setIsFollowing(data);
          } else {
            console.warn(`‚ùå ÌåîÎ°úÏö∞ ÏÉÅÌÉú ÌôïÏù∏ Ïã§Ìå® (${res.status})`);
            setIsFollowing(false);
          }
        } catch (err) {
          console.error("‚ùå ÌåîÎ°úÏö∞ ÏÉÅÌÉú ÌôïÏù∏ Ïã§Ìå®:", err);
          setIsFollowing(false);
        }
      } else {
        setIsFollowing(false);
      }
    };
    checkFollowingStatus();

    // Í≤åÏãúÍ∏ÄÍ≥º Ï¢ãÏïÑÏöî Ï†ïÎ≥¥ Í∞ÄÏ†∏Ïò§Í∏∞
    const fetchPostsAndLikes = async () => {
      try {
        const res = await fetch(`${baseUrl}/followers/getPost/${userId}`, {
          method: "GET",
          headers: {
            "Content-Type": "application/json",
            Authorization: `Bearer ${token}`,
          },
        });
        if (!res.ok) throw new Error("Í≤åÏãúÍ∏Ä Î∂àÎü¨Ïò§Í∏∞ Ïã§Ìå®");

        const posts: Post[] = await res.json();
        const artPosts = posts.filter((post) => post.boardNo === 5);

        const postsWithImageUrls = artPosts.map((post) => {
          const imagePath =
            post.pictureDTOList && post.pictureDTOList.length > 0
              ? post.pictureDTOList[0].resizedImagePath ||
                post.pictureDTOList[0].thumbnailImagePath ||
                post.pictureDTOList[0].originImagePath
              : null;

          const imageUrl = imagePath
            ? `${imageBaseUrl}${imagePath}`
            : "/default-image.png";

          return {
            ...post,
            imageUrl,
          };
        });

        const postsWithLikes = await Promise.all(
          postsWithImageUrls.map(async (post) => {
            if (isNaN(loggedInUserId) || !post.postId) {
              return { ...post, liked: false, favoriteCnt: 0 };
            }

            try {
              const likedRes = await fetch(
                `${baseUrl}/favorites/${loggedInUserId}/${post.postId}`,
                {
                  method: "GET",
                  headers: {
                    "Content-Type": "application/json",
                    Authorization: `Bearer ${token}`,
                  },
                  credentials: "include",
                }
              );
              const liked = JSON.parse(await likedRes.text());

              const countRes = await fetch(
                `${baseUrl}/favorites/count/${post.postId}`,
                {
                  method: "GET",
                  headers: {
                    "Content-Type": "application/json",
                    Authorization: `Bearer ${token}`,
                  },
                  credentials: "include",
                }
              );
              const countData = await countRes.json();
              const count =
                typeof countData === "number"
                  ? countData
                  : countData.count || 0;

              return { ...post, liked, favoriteCnt: count };
            } catch (err) {
              console.error(
                `‚ùå Like data fetch Ïã§Ìå®: post ${post.postId}`,
                err
              );
              return { ...post, liked: false, favoriteCnt: 0 };
            }
          })
        );

        setCardData(postsWithLikes);
      } catch (err) {
        console.error("‚ùå Í≤åÏãúÍ∏Ä/Ï¢ãÏïÑÏöî Î∂àÎü¨Ïò§Í∏∞ Ïã§Ìå®:", err);
      }
    };

    fetchPostsAndLikes();
  }, [userId, loggedInUserId]);

  const handleFollowToggle = async () => {
    if (
      isNaN(loggedInUserId) ||
      isNaN(userId) ||
      loggedInUserId === 0 ||
      userId === 0 ||
      loggedInUserId === userId
    ) {
      console.warn("üî¥ ÌåîÎ°úÏö∞ Ï°∞Í±¥ ÎØ∏Ï∂©Ï°±", { loggedInUserId, userId });
      if (isNaN(loggedInUserId) || loggedInUserId === 0) {
        alert("ÌåîÎ°úÏö∞ Í∏∞Îä•ÏùÑ ÏÇ¨Ïö©ÌïòÎ†§Î©¥ Î°úÍ∑∏Ïù∏Ïù¥ ÌïÑÏöîÌï©ÎãàÎã§.");
      }
      return;
    }

    const token = localStorage.getItem("token");
    const isNowFollowing = !isFollowing;
    const method = isNowFollowing ? "POST" : "DELETE";
    const url = isNowFollowing
      ? `${baseUrl}/followers/${loggedInUserId}/follow/${userId}`
      : `${baseUrl}/followers/${loggedInUserId}/unfollow/${userId}`;

    setIsFollowing(isNowFollowing);
    setFollowCnt((prev) => prev + (isNowFollowing ? 1 : -1));

    try {
      const res = await fetch(url, {
        method,
        headers: {
          "Content-Type": "application/json",
          Authorization: `Bearer ${token}`,
        },
      });
      if (!res.ok) {
        const errorText = await res.text();
        console.error(`‚ùå ÌåîÎ°úÏö∞ ÏöîÏ≤≠ Ïã§Ìå® (${res.status}):`, errorText);
        setIsFollowing(!isNowFollowing);
        setFollowCnt((prev) => prev + (isNowFollowing ? -1 : 1));
        alert(`ÌåîÎ°úÏö∞ ÏöîÏ≤≠ Ïã§Ìå®: ${errorText || res.statusText}`);
        throw new Error(`ÌåîÎ°úÏö∞ ÏöîÏ≤≠ Ïã§Ìå® (${res.status})`);
      }

      const data = await res.json();
      if (typeof data.followCnt === "number") setFollowCnt(data.followCnt);
      if (typeof data.followingCnt === "number")
        setFollowingCnt(data.followingCnt);

      console.log(isNowFollowing ? "ÌåîÎ°úÏö∞ ÏÑ±Í≥µ" : "ÌåîÎ°úÏö∞ Ï∑®ÏÜå ÏÑ±Í≥µ", data);
    } catch (err) {
      console.error("‚ùå ÌåîÎ°úÏö∞ ÌÜ†Í∏Ä Ïã§Ìå®:", err);
    }
  };

  const handleOpenChat = () => {
    navigate("/chat");
  };

  const handleCardClick = (id: number) => {
    navigate(`/Art/${id}`);
  };

  const handleLikeToggle = async (postId: number) => {
    if (isNaN(loggedInUserId) || loggedInUserId === 0) {
      alert("Ï¢ãÏïÑÏöî Í∏∞Îä•ÏùÑ ÏÇ¨Ïö©ÌïòÎ†§Î©¥ Î°úÍ∑∏Ïù∏Ïù¥ ÌïÑÏöîÌï©ÎãàÎã§.");
      navigate("/login");
      return;
    }

    const token = localStorage.getItem("token");

    setCardData((prev) =>
      prev.map((card) => {
        if (card.postId === postId) {
          const newLiked = !card.liked;
          const newFavoriteCnt = (card.favoriteCnt ?? 0) + (newLiked ? 1 : -1);
          return {
            ...card,
            liked: newLiked,
            favoriteCnt: newFavoriteCnt,
          };
        }
        return card;
      })
    );

    try {
      const result = await fetch(`${baseUrl}/favorites/toggle`, {
        method: "POST",
        headers: {
          "Content-Type": "application/json",
          Authorization: `Bearer ${token}`,
        },
        body: JSON.stringify({
          userId: loggedInUserId,
          postId: postId,
        }),
      });

      if (!result.ok) {
        const errorText = await result.text();
        console.error(
          `‚ùå Ï¢ãÏïÑÏöî ÌÜ†Í∏Ä ÏÑúÎ≤Ñ ÏùëÎãµ Ïò§Î•ò (${result.status}):`,
          errorText
        );
        throw new Error(`ÏÑúÎ≤Ñ ÏùëÎãµ Ïò§Î•ò: ${errorText || result.statusText}`);
      }

      const data = await result.json();

      if (typeof data.favoriteCount === "number") {
        setCardData((prev) =>
          prev.map((card) =>
            card.postId === postId
              ? {
                  ...card,
                  liked: data.favorited,
                  favoriteCnt: data.favoriteCount,
                }
              : card
          )
        );
      } else {
        console.warn(
          "‚ùå Ï¢ãÏïÑÏöî ÌÜ†Í∏Ä API ÏùëÎãµÏóê favoriteCountÍ∞Ä ÏóÜÏäµÎãàÎã§.",
          data
        );
      }
    } catch (error) {
      console.error("Ï¢ãÏïÑÏöî Ï≤òÎ¶¨ Ïã§Ìå®", error);

      setCardData((prev) =>
        prev.map((card) => {
          if (card.postId === postId) {
            const rolledBackLiked = !card.liked;
            const rolledBackFavoriteCnt =
              (card.favoriteCnt ?? 0) + (rolledBackLiked ? 1 : -1);
            return {
              ...card,
              liked: rolledBackLiked,
              favoriteCnt: rolledBackFavoriteCnt,
            };
          }
          return card;
        })
      );
      alert("Ï¢ãÏïÑÏöî Ï≤òÎ¶¨Ïóê Ïã§Ìå®ÌñàÏäµÎãàÎã§. Îã§Ïãú ÏãúÎèÑÌï¥Ï£ºÏÑ∏Ïöî.");
    }
  };

  return (
    <div className="worker-container">
      <div className="worker-header">
        <img
          src={
            profile?.thumbnailImagePath
              ? profile.thumbnailImagePath.startsWith("/ourlog")
                ? `http://localhost:8080${profile.thumbnailImagePath}`
                : `${imageBaseUrl}${profile.thumbnailImagePath}`
              : "/mypage.png"
          }
          alt="ÌîÑÎ°úÌïÑ Ïù¥ÎØ∏ÏßÄ"
          className="worker-profile-img"
          onError={(e) => {
            const target = e.target as HTMLImageElement;
            target.onerror = null;
            target.src = "/mypage.png";
          }}
        />
        <div className="worker-info">
          <div className="worker-meta-row">
            <div className="worker-name">
              {profile?.nickname || "ÎãâÎÑ§ÏûÑ ÏóÜÏùå"}
            </div>
            <div className="worker-stats">
              <div className="stat">
                <span className="label">ÌåîÎ°úÏö∞</span>
                <span>{followCnt}</span>
              </div>
              <div className="stat">
                <span className="label">ÌåîÎ°úÏûâ</span>
                <span>{followingCnt}</span>
              </div>
            </div>
          </div>
          <div className="worker-buttons">
            {!isNaN(loggedInUserId) && loggedInUserId !== userId && (
              <button
                onClick={handleFollowToggle}
                className={`btn ${isFollowing ? "following" : ""}`}
              >
                {isFollowing ? "ÌåîÎ°úÏûâ" : "ÌåîÎ°úÏö∞"}
              </button>
            )}
            {!isNaN(loggedInUserId) && loggedInUserId !== userId && (
              <button className="btn" onClick={handleOpenChat}>
                Ï±ÑÌåÖÏ∞Ω
              </button>
            )}
          </div>
        </div>
      </div>

      <section className="worker-gallery">
        {currentCards.map((card) => (
          <div
            key={card.postId}
            className="worker-card"
            onClick={() => handleCardClick(card.postId)}
            style={{ cursor: "pointer", position: "relative" }}
          >
            <figure className="card-image-wrapper">
              <img
                src={card.imageUrl || "/default-image.png"}
                alt={`ÏûëÌíà ${card.postId}`}
                className="card-image"
                onError={(e) => {
                  const target = e.target as HTMLImageElement;
                  target.onerror = null;
                  target.src = "/default-image.png";
                }}
              />
              {!isNaN(loggedInUserId) &&
                loggedInUserId !== 0 &&
                card.postId !== undefined &&
                card.postId !== null && (
                  <button
                    className={`worker-like-button ${
                      card.liked ? "liked" : ""
                    }`}
                    onClick={(e) => {
                      e.stopPropagation();
                      handleLikeToggle(card.postId);
                    }}
                  >
                    {card.liked ? "üß°" : "ü§ç"}{" "}
                    <span>{card.favoriteCnt ?? 0}</span>
                  </button>
                )}
            </figure>
            <div className="card-body">
              <h2 className="card-title">{card.title}</h2>
            </div>
          </div>
        ))}
      </section>

      <div className="pagination">
        <nav aria-label="ÌéòÏù¥ÏßÄÎÑ§Ïù¥ÏÖò">
          <button
            onClick={() => setCurrentPage((prev) => Math.max(prev - 1, 1))}
            className="page-btn"
            disabled={currentPage === 1}
          >
            &lt;
          </button>
          {[...Array(totalPages)].map((_, i) => (
            <button
              key={i + 1}
              onClick={() => setCurrentPage(i + 1)}
              className={`page-btn ${currentPage === i + 1 ? "active" : ""}`}
            >
              {i + 1}
            </button>
          ))}
          <button
            onClick={() =>
              setCurrentPage((prev) => Math.min(prev + 1, totalPages))
            }
            className="page-btn"
            disabled={currentPage === totalPages}
          >
            &gt;
          </button>
        </nav>
      </div>
    </div>
  );
};

export default WorkerPage;
